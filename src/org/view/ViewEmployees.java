    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField6.getText().trim().isEmpty()){
            int ID = Integer.parseInt(jTextField6.getText());
            Object[] row =user.Search(ID);
            if(row!=null){
           
                EmpID.setText(row[0].toString());
                EmpFname.setText(row[1].toString());
                EmpLName.setText(row[2].toString());
                EmpAge.setText(row[3].toString());
                EmpEmail.setText(row[4].toString());
                DepName.setText(row[5].toString());
                int row1 = user.Delete(ID);
                if(row1>0){
                    JOptionPane.showMessageDialog(null, "Employee Record deleted successfully");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Employee record deletion failed");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "The entered ID was not found");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter a valid Employee ID To Search");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

