    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!EmpID.getText().trim().isEmpty() &&!EmpFname.getText().trim().isEmpty() &&!EmpLName.getText().trim().isEmpty() &&!EmpAge.getText().trim().isEmpty() &&!EmpEmail.getText().trim().isEmpty() &&!DepName.getText().trim().isEmpty()){
            emp.setUserId(Integer.parseInt(EmpID.getText()));
            emp.setF_name(EmpFname.getText());
            emp.setL_name(EmpLName.getText());
            emp.setAge(Integer.parseInt(EmpAge.getText()));
            emp.setEmail(EmpEmail.getText());
            emp.setDep(DepName.getText());
            int row = user.employees(emp);
            if(row>0){
                JOptionPane.showMessageDialog(null, "Employee Registered Successfully");
            }
            else{
                JOptionPane.showMessageDialog(null, "Employee Registeration Failed");
            }
           
        }
        else{
            JOptionPane.showMessageDialog(null, "Fill all the required fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

