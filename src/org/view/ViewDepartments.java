    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(!jTextField5.getText().trim().isEmpty()){
            int ID = Integer.parseInt(jTextField5.getText());
            Object[] row =user.Search(ID);
            if(row!=null){
           
                DepID.setText(row[0].toString());
                DepName.setText(row[1].toString());
                DepLocation.setText(row[2].toString());
                DepHead.setText(row[3].toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "The enterd ID was not found");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter A valid Department ID To Search");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField5.getText().trim().isEmpty()){
            int ID = Integer.parseInt(jTextField5.getText());
            Object[] row =user.Search(ID);
            if(row!=null){
           
                DepID.setText(row[0].toString());
                DepName.setText(row[1].toString());
                DepLocation.setText(row[2].toString());
                DepHead.setText(row[3].toString());
                int row1 = user.Delete(ID);
                if(row1>0){
                    JOptionPane.showMessageDialog(null, "Candidate Record deleted successfully");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Employee record deletion failed");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "The entered ID was not found");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter A User ID To Search");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

