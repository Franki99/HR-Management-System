    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!DepID.getText().trim().isEmpty() &&!DepName.getText().trim().isEmpty() &&!DepLocation.getText().trim().isEmpty() &&!DepHead.getText().trim().isEmpty()){
            emp.setD_Id(Integer.parseInt(DepID.getText()));
            emp.setD_name(DepName.getText());
            emp.setD_location(DepLocation.getText());
            emp.setD_Head(DepHead.getText());
            int row = user.departments(emp);
            if(row>0){
                JOptionPane.showMessageDialog(null, "Department Registered Successfully");
            }
            else{
                JOptionPane.showMessageDialog(null, "Department Registeration Failed");
            }
           
        }
        else{
            JOptionPane.showMessageDialog(null, "Fill all the required fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

