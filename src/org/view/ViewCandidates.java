    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!CandID.getText().trim().isEmpty() &&!CandFname.getText().trim().isEmpty() &&!CandLname.getText().trim().isEmpty() &&!CandAge.getText().trim().isEmpty() &&!Edu.getText().trim().isEmpty() &&!CandEmail.getText().trim().isEmpty()){
            emp.setUserId(Integer.parseInt(CandID.getText()));
            emp.setF_name(CandFname.getText());
            emp.setL_name(CandLname.getText());
            emp.setAge(Integer.parseInt(CandAge.getText()));
            emp.setEmail(CandEmail.getText());
            emp.setEdu(Edu.getText());
            int row = user.candidates(emp);
            if(row>0){
                JOptionPane.showMessageDialog(null, "Candidate Registered Successfully");
            }
            else{
                JOptionPane.showMessageDialog(null, "Candidate Registeration Failed");
            }
           
        }
        else{
            JOptionPane.showMessageDialog(null, "Fill all the required fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

