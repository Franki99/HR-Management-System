    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(!jTextField6.getText().trim().isEmpty()){
            int ID = Integer.parseInt(jTextField6.getText());
            Object[] row =user.Search(ID);
            if(row!=null){
           
                CandID.setText(row[0].toString());
                CandFname.setText(row[1].toString());
                CandLname.setText(row[2].toString());
                CandAge.setText(row[3].toString());
                CandEmail.setText(row[4].toString());
                Edu.setText(row[5].toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "The enterd ID was not found");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter a valid candidate ID to Search");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField6.getText().trim().isEmpty()){
            int ID = Integer.parseInt(jTextField6.getText());
            Object[] row =user.Search(ID);
            if(row!=null){
           
                CandID.setText(row[0].toString());
                CandFname.setText(row[1].toString());
                CandLname.setText(row[2].toString());
                CandAge.setText(row[3].toString());
                CandEmail.setText(row[4].toString());
                Edu.setText(row[5].toString());
                int row1 = user.Delete(ID);
                if(row1>0){
                    JOptionPane.showMessageDialog(null, "Candidate Record deleted successfully");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Candidate record deletion failed");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "The entered ID was not found");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter a valid candidate ID To Search");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

